function add(n1,n2){
    return n1 + n2;
}

function subtract(n1,n2){
    return n1 - n2;
}

function multiply(n1,n2){
    return n1 * n2;
}

function devide(n1,n2){
    return n1 / n2;
}

function mod(n1,n2){
    return n1 % n2;
}

var pi = 3.1415;

export  {
    add,
    subtract,
    multiply,
    devide,
    mod,
    pi
}